function createEnvelope(index, message) {
    const envelope = document.createElement('div');
    envelope.className = 'envelope';
    
    // Random position based on actual container size
    const containerWidth = envelopesArea.offsetWidth - 100;
    const containerHeight = envelopesArea.offsetHeight - 100;
    const randomX = Math.random() * containerWidth;
    const randomY = Math.random() * containerHeight;
    const randomRotation = (Math.random() - 0.5) * 30;
    
    envelope.style.left = `${randomX}px`;
    envelope.style.top = `${randomY}px`;
    envelope.style.setProperty('--rotation', `${randomRotation}deg`);
    envelope.style.animationDelay = `${index * 0.05}s`;
    
    // Random color for envelope
    const colors = ['#FFD93D', '#6BCB77', '#4D96FF', '#FFB6D9', '#A084DC', '#FF8989'];
    const color = colors[Math.floor(Math.random() * colors.length)];
    
    envelope.innerHTML = `
        <svg viewBox="0 0 100 80" xmlns="http://www.w3.org/2000/svg">
            <rect x="10" y="20" width="80" height="50" fill="${color}" stroke="#333" stroke-width="2" rx="2"/>
            <path d="M10 20 L50 50 L90 20" fill="none" stroke="#333" stroke-width="2"/>
            <path d="M10 70 L50 50 L90 70" fill="${color}" stroke="#333" stroke-width="2"/>
        </svg>
    `;
    
    envelope.addEventListener('click', () => {
        openMessage(envelope, message, index);
    });
    
    return envelope;
}

function openMessage(envelopeElement, message, index) {
    if (selectedEnvelopes.has(index)) return;
    
    selectedEnvelopes.add(index);
    envelopeElement.classList.add('opened');
    
    messageContent.textContent = message;
    modal.classList.add('active');
}

function closeModalHandler() {
    modal.classList.remove('active');
}

// Keyboard support
